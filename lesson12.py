#Matematiksel İşlemler (Math Operations)

#Toplama - Addition
a = 10
b = 5
result = a + b
print(f"The result is: {result}")

#Çıkarma - Substruction
result = a-b
print(f"The result is: {result}")

#Çarpma - Multiplication
result = a*b
print(f"The result is: {result}")

#Bölme - Division
result = a/b
print(f"The result is: {result}") # Result type is float by default

#Tam Sayılı Bölme - Floor Division
result = a//b
print(f"The result is: {result}")

#Kalan - Modulus
result = a%b
print(f"The result is: {result}")

#İşlem Sırası - Order of operations(PEMDAS)
